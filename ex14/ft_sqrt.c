int	ft_sqrt(int nb)
{
	int	i;

	if (nb <= 0)
		return (0);
	i = 1;
	while (i * i < nb)
	{
		if (i >= 46341)
			return (0);
		++i;
	}
	if (i * i == nb)
		return (i);
	else
		return (0);
}
